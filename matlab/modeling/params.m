function p = params()

% ==========================================
% Physical Constants
% ==========================================

p.g = 9.81;                 % m/s^2

% ==========================================
% Masses
% ==========================================

p.m_body  = 0.0614;         % kg
p.m_wheel = 0.03432;        % kg

p.m_total = p.m_body + p.m_wheel;

% ==========================================
% Geometry
% ==========================================

p.l_com = 0.084;            % m
p.l_wheel = 0.080;          % m

% ==========================================
% Inertias
% ==========================================

p.Jp = 1.34e-3;             % kg*m^2

p.Jw = 1.3183e-4;           % kg*m^2

% ==========================================
% Motor Parameters
% ==========================================

p.deadzone_pwm = 50;

p.motor_gain_rpm = 1.1580;          % RPM/PWM
p.motor_gain_rad = 0.1213;          % rad/s per PWM

p.tau_motor = 0.30;                 % s

% ==========================================
% Experimental References
% ==========================================

p.lambda_exp = 8.115;       % rad/s

end
